const std = @import("std");
const zbench = @import("zbench");
const stun = @import("stun");

// Binding request, no attributes (header only)
const empty_request = [_]u8{
    0x00, 0x01, 0x00, 0x00,
    0x21, 0x12, 0xA4, 0x42,
    0x00, 0x01, 0x02, 0x03,
    0x04, 0x05, 0x06, 0x07,
    0x08, 0x09, 0x0A, 0x0B,
};

// Binding success response with MAPPED-ADDRESS (IPv4) + XOR-MAPPED-ADDRESS (IPv4)
const mapped_response = [_]u8{
    0x01, 0x01, 0x00, 0x18,
    0x21, 0x12, 0xA4, 0x42,
    0x00, 0x01, 0x02, 0x03,
    0x04, 0x05, 0x06, 0x07,
    0x08, 0x09, 0x0A, 0x0B,
    // MAPPED-ADDRESS IPv4
    0x00, 0x01, 0x00, 0x08,
    0x00, 0x01, 0x80, 0x55,
    192,  0,    2,    1,
    // XOR-MAPPED-ADDRESS IPv4
    0x00, 0x20, 0x00, 0x08,
    0x00, 0x01, 0xA1, 0x47,
    0xE1, 0x12, 0xA6, 0x43,
};

